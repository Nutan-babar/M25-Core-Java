public class DemoOnDouble {

	public static void main(String[] args) {
		
		// Double Datatype
				Scanner s=new Scanner(System.in);
				System.out.println("Enter the value : ");
				Double x=s.nextDouble();
				System.out.println("The result will be: ");
				System.out.print(x);

	}

}
